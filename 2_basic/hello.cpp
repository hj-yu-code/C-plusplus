#include <iostream> // 입출력 활용하는 객체지향 라이브러리

int main(){
    int num = 100;
    // std : iostream에 있는 객체들이 std라는 namespace에 정의 되어 있음
    std::cout << "hello world" << std::endl;
    std::cout << "인피니트";
    std::cout << "헬스케어" << std::endl;
    std::cout << "num : " << num << std::endl;
    std::cout << 'A' << ", " << 3.14 << std::endl;

    return 0;
}